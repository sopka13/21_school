#include "libft.h"

char	*ft_strtoupper(char *c)
{
	int	i;

	i = 0;
	if (!c)
		return (NULL);
	while (c[i] != '\0')
	{
		if (c[i] >= 'a' && c[i] <= 'z')
			c[i] = (c[i] - 32);
		i++;
	}
	return (c);
}
