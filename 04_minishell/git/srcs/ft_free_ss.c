#include "../minishell.h"

void		ft_free_ss(char **str)
{
	int		i;

	i = -1;
	if (str == 0x0)
		return ;
	else
	{
		while (str[++i])
			free(str[i]);
	}
	free(str);
	str = NULL;
	return ;
}
