/*
** Check character for a valid name env var
** 	return (1) - Have delimiter character
** 	return (0) - Valid character
*/

#include "../minishell.h"

int			ft_valid_env_chr(char a)
{
	if (a == ' ' || a == '!' || a == '#' || a == '\t' || a == '%' ||
		a == '*' || a == '+' || a == ',' || a == '-' || a == '.' ||
		a == '/' || a == '[' || a == ']' || a == '^' || a == '@' ||
		a == '\"' || a == '\'' || a == '$' || a == '&' || a == '(' ||
		a == ')' || a == ':' || a == ';' || a == '<' || a == '=' ||
		a == '>' || a == '?' || a == '{' || a == '}' || a == '|' ||
		a == '~' || a == '\0')
		return (0);
	else
		return (1);
}
