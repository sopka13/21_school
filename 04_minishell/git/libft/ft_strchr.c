#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i] && s[i] != (char)c)
	{
		i++;
	}
	return (s[i] == (char)c ? (char*)s + i : NULL);
}
