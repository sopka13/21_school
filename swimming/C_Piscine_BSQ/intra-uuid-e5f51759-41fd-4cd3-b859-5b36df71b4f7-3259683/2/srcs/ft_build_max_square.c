#include "head.h"

void		ft_build_max_square(int i, int j, int min)
{
	int p;
	int q;

	p = 0;
	q = 0;
	while (p < min)
	{
		q = 0;
		while (q < min)
		{
			g_map[i - p][j - q] = g_full;
			q++;
		}
		p++;
	}
}
