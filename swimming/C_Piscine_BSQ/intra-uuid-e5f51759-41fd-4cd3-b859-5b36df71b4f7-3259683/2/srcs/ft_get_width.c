#include "head.h"

void	ft_get_width(char *str_map)
{
	int i;

	i = 0;
	g_width = 0;
	while (str_map[i] != '\n')
		i++;
	i++;
	while (str_map[i] != '\n')
	{
		i++;
		g_width++;
	}
}
