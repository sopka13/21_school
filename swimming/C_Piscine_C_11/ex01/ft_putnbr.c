#include <unistd.h>

void	ft_prt(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int a;

	a = nb;
	if (a < 0)
	{
		ft_prt('-');
		a = a * (-1);
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	else
	{
		ft_prt(a + '0');
	}
}
