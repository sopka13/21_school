#include <stdio.h>

int	ft_count_if(char **tab, int (*f)(char*))
{
	int		i;
	int		k;
	char	*tb;

	tb = *tab;
	k = 0;
	i = 0;
	while (tb[i] != '\0')
	{
		if (f(&tb[i]) != 0)
		{
			k++;
			i++;
		}
		else
			i++;
	}
	return (k);
}
