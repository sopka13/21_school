int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int i;
	int o;
	int p;
	int q;

	o = 0;
	p = 0;
	q = 0;
	i = 1;
	while (i <= length)
	{
		if (f(tab[i - 1], tab[i]) == 1)
			o++;
		if (f(tab[i - 1], tab[i]) == 0)
			p++;
		else
			q++;
		i++;
	}
	if ((o >= 0 && p >= 0 && q == 0) || (q >= 0 && p >= 0 && o == 0))
		return (1);
	if (q > 0 && o > 0)
		return (0);
	return (0);
}
