#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int		i;
	int		k;

	i = 0;
	while ('0' <= str[i] && str[i] <= '9')
	{
		i++;
	}
	if (str[i] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
