#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (s1[i] == s2[i] && i < (n - 1))
	{
		i++;
	}
	if (i == 0)
	{
		return (0);
	}
	else
	{
		if (s1[i - 1] == '\0')
		{
			return (0);
		}
		else
		{
			return (s1[i] - s2[i]);
		}
	}
}
